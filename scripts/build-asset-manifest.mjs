#!/usr/bin/env node
/**
 * Walks public/assets/ after curation and emits:
 *   - src/assets/manifest.generated.ts — typed `as const` manifest
 *   - docs/asset-inventory.md — appends/replaces a "Curated for RC" section
 *
 * Categories inferred from path: tilesets/, models/, audio/.
 * Biome inferred from path segment when present (meadow/forest/coast/grove).
 */

import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, extname, join, posix, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const PUBLIC_ASSETS = join(ROOT, "public", "assets");
const MANIFEST_OUT = join(ROOT, "src", "assets", "manifest.generated.ts");
const INVENTORY_DOC = join(ROOT, "docs", "asset-inventory.md");

const BIOMES = ["meadow", "forest", "coast", "grove", "menu", "ui"];

function walk(dir) {
  if (!existsSync(dir)) return [];
  const out = [];
  for (const name of readdirSync(dir)) {
    if (name.startsWith(".")) continue;
    const full = join(dir, name);
    const s = statSync(full);
    if (s.isDirectory()) out.push(...walk(full));
    else if (s.isFile()) out.push({ full, size: s.size });
  }
  return out;
}

const files = walk(PUBLIC_ASSETS);

function categorize(rel) {
  if (rel.startsWith("tilesets/")) return "tileset";
  if (rel.startsWith("models/")) return "model";
  if (rel.startsWith("audio/music/")) return "music";
  if (rel.startsWith("audio/sfx/")) return "sfx";
  if (rel.startsWith("audio/ambient/")) return "ambient";
  if (rel.startsWith("audio/")) return "audio";
  return "misc";
}

function biomeOf(rel) {
  const parts = rel.split("/");
  for (const p of parts) if (BIOMES.includes(p)) return p;
  return "all";
}

function formatOf(rel) {
  return extname(rel).slice(1).toLowerCase();
}

const entries = files.map((f) => {
  const rel = posix.normalize(relative(PUBLIC_ASSETS, f.full).split(/\\|\//).join("/"));
  return {
    path: `assets/${rel}`,
    rel,
    category: categorize(rel),
    biome: biomeOf(rel),
    format: formatOf(rel),
    size: f.size,
  };
});

entries.sort((a, b) => a.rel.localeCompare(b.rel));

const lines = [
  "// AUTO-GENERATED by scripts/build-asset-manifest.mjs",
  "// Do not edit by hand. Run `pnpm assets:manifest` to regenerate.",
  "",
  "export const ASSET_MANIFEST = [",
];
for (const e of entries) {
  lines.push(
    `  { path: ${JSON.stringify(e.path)}, category: ${JSON.stringify(e.category)}, biome: ${JSON.stringify(e.biome)}, format: ${JSON.stringify(e.format)}, size: ${e.size} },`,
  );
}
lines.push("] as const;");
lines.push("");
lines.push("export type AssetManifestEntry = typeof ASSET_MANIFEST[number];");
lines.push("export type AssetCategory = AssetManifestEntry['category'];");
lines.push("export type AssetBiome = AssetManifestEntry['biome'];");
lines.push("");
lines.push("export const ASSET_COUNT = ASSET_MANIFEST.length;");
lines.push("");

import("node:fs/promises").then(async (fsp) => {
  await fsp.mkdir(dirname(MANIFEST_OUT), { recursive: true });
  await fsp.writeFile(MANIFEST_OUT, lines.join("\n"), "utf8");
  console.log(`Wrote ${entries.length} entries to ${relative(ROOT, MANIFEST_OUT)}`);

  // Update inventory doc with "Curated for RC" section.
  const SECTION_MARK = "## Curated for RC (generated)";
  const totals = entries.reduce(
    (acc, e) => {
      acc.total++;
      acc.bytes += e.size;
      acc.byCategory[e.category] = (acc.byCategory[e.category] ?? 0) + 1;
      return acc;
    },
    { total: 0, bytes: 0, byCategory: {} },
  );

  const section = [
    SECTION_MARK,
    "",
    `_Last regenerated: ${new Date().toISOString().slice(0, 10)} by \`pnpm assets:manifest\`._`,
    "",
    "This section reflects what currently lives under `public/assets/` and ships with the game,",
    "as opposed to the source-side inventory above which catalogues raw `voxel-realms/raw-assets/extracted/` packs.",
    "",
    `**Total assets:** ${totals.total}`,
    `**Total size:** ${(totals.bytes / 1024 / 1024).toFixed(1)} MB`,
    "",
    "**By category:**",
    "",
    ...Object.entries(totals.byCategory)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([k, v]) => `- ${k}: ${v}`),
    "",
    "**Manifest file:** `src/assets/manifest.generated.ts`",
    "",
  ].join("\n");

  let doc = "";
  try {
    doc = readFileSync(INVENTORY_DOC, "utf8");
  } catch {
    /* doc may not exist on a fresh clone; that's fine */
  }
  if (doc.includes(SECTION_MARK)) {
    doc = doc.replace(
      new RegExp(`${SECTION_MARK}[\\s\\S]*?(?=\\n## |$)`),
      section,
    );
  } else {
    doc = doc.trimEnd() + "\n\n" + section + "\n";
  }
  writeFileSync(INVENTORY_DOC, doc, "utf8");
  console.log(`Updated ${relative(ROOT, INVENTORY_DOC)}`);
});
